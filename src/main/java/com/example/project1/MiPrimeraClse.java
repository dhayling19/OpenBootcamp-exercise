package com.example.project1;

public class MiPrimeraClse {
    public static void main(String[] args) {

        byte number = 1;
        System.out.println("Un byte es " + number);

        short number2 = 2;
        System.out.println("Un short es " + number2);

        int number3 = 3;
        System.out.println("Un int es " + number3);

        long number4 = 4;
        System.out.println("Un long es " + number4);

        float decimal = 5.9f;
        System.out.println("Un float es " + decimal);


        double decimal2 = 6.33;
        System.out.println("Un float es " + decimal2);

        char caracter1 = 'd';
        System.out.println("Un caracter es "+ caracter1);


        String color = "Rosado";
        System.out.println("Estas viendo un String que contiene un color  "+ color);

        boolean verdad = true;
        System.out.println("Si dices la verdad es " + verdad);

        boolean mentira = false;
        System.out.println("Si dices mentiras es " + mentira);


    }
}
